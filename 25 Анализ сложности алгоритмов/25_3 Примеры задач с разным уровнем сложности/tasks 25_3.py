# Количество элементов

# Напиши функцию, которая пройдет по массиву и подсчитает сколько каких элементов
# встречается.

def count_elements(arr):
    rez = {}
    for i in arr:
        rez[i] = rez.get(i, 0) + 1
    return rez


# Пример использования:
arr = [1, 2, 2, 3, 3, 3, 4, 4, 4, 4]
print(count_elements(arr))  # Output: {1: 1, 2: 2, 3: 3, 4: 4}


