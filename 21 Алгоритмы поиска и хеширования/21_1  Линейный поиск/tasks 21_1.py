# Напишите функцию, которая выполняет линейный поиск для проверки наличия заданного
# элемента в списке строк.
# Функция должна возвращать True, если элемент найден, и False, если элемент
# отсутствует.
# Пользоваться классом list можно, вызывать его функции - нет.

# Напишите тут ваш код

def linear_search(arr, target):
    for element in arr:
        if element == target:
            return True
    return False


