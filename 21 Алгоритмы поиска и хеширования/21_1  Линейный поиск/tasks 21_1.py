# Линейный поиск

# Напишите функцию, которая выполняет линейный поиск заданного элемента в массиве
# чисел.
# Функция должна возвращать индекс первого найденного элемента или -1, если элемент
# не найден.
# Пользоваться классом list можно, вызывать его функции - нет.

# Напишите тут ваш код
def linear_search(arr, target):
    for index, element in enumerate(arr):
        if element == target:
            return index
    return -1


