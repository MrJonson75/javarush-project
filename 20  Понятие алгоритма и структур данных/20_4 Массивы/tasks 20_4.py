# Двумерный массив.

# Создание одномерного массива для имитации двумерного массива 10x10
# Заполнение массива как таблицы умножения x * y
# Примечание: х от 1 до 10 и y от 1 до 10.
# Класс list использовать нельзя.

# Напишите тут ваш код

import array

# Создаем одномерный массив размером 10x10 = 100 элементов
table = array.array('i', [0] * 100)

# Заполняем таблицу умножения (x и y от 1 до 10)
for i in range(10):
    for j in range(10):
        table[i * 10 + j] = (i + 1) * (j + 1)  # Формула для индексации

# Выводим таблицу
for i in range(10):
    for j in range(10):
        print(f"{table[i * 10 + j]:4}", end='')
    print()  # Переход на новую строку